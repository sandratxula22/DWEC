//lenguaje_21.js - Estructuras de control: if

//IF
if(4>2) {
	console.log("4 es mayor que 2");
}

//IF...ELSE
if(4>6) {
	console.log("4 es mayor que 6");
}
else {
	console.log("4 no es mayor que 6");
}

//IF...ELSE IF...ELSE	
if(4>6) {
	console.log("4 es mayor que 6");
}
else if (4>3) {
	console.log("4 es mayor que 3");
}
else {
	console.log("ninguna de las anteriores");
}