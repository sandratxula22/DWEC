//Función que no hace nada. Toda función sin return devuelve undefined
function funcion0()	{
}

//Función sin parámetros. No devuelve valores
function funcion1() {
	console.log("Escribir un texto");
}

//Función sin parámetros. Devuelve valores
function funcion2()	{
	return 2;
}

//Función con parámetros. Devuelve valores
function funcion3(a,b) {
	return a+b;
}

	
funcion0();

	
funcion1();


console.log(funcion2());
			
		
console.log(funcion3(2,3));